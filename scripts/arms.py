"""Arm registry — the single place an arm standard is described (PLAN: multi-arm onboarding).

An ArmSpec carries everything the SIM/teleop stack needs to load and drive an arm:
models, EE frames, joint counts, the XRoboToolkit manipulator config. Driver/serve fields
(real hardware) are documented per arm in docs/refs/<arm>/INDEX.md and wired later —
sim-first is the onboarding path (browser console at :8810, no hardware required).

Conventions every spec must satisfy:
  - mjcf scene includes a `home` keyframe and a `<side>_target` mocap body per side
    (+ offwidth >= 1920 for the stereo canvas).
  - ee_link  = URDF/placo frame the IK tracks; ee_body = MJCF body for FK/HUD readouts
    (often the same name; SO-101 differs).
  - dof = ARM joints only (gripper excluded); the sim gripper joint, when modeled,
    is named in gripper_joint.
"""

from dataclasses import dataclass, field
from typing import Optional


@dataclass(frozen=True)
class ArmSpec:
    name: str
    dof: int                                  # arm joints, gripper EXCLUDED
    mjcf: str                                 # scene with home keyframe + mocap target(s)
    urdf: str                                 # for placo IK
    ee_link: str                              # URDF frame the IK tracks
    ee_body: str                              # MJCF body for FK/HUD
    manipulators: dict = field(default_factory=dict)   # MujocoTeleopController config
    gripper_joint: Optional[str] = None       # sim gripper ACTUATOR name (None = not modeled);
                                              # the trigger drives d.ctrl for this actuator
    gripper_open_high: bool = True            # True: high ctrl = open (SO-101 hinge);
                                              # False: low ctrl = open (Robotiq 0=open,255=closed)
    orientation_weight: float = 1.0           # IK orientation vs position (1.0 = equal).
                                              # <1 for arms with <6 DOF: position stays
                                              # pinned, orientation becomes best-effort so
                                              # unreachable rotations go to the WRIST's
                                              # spare DOFs instead of dragging the arm.
    max_vel: float = 0.6                      # rad/s joint-speed cap (teleop/policy/home)
    home_key: str = "home"
    status: str = "ready"                     # ready | assets-pending
    notes: str = ""


def _single(ee_link, vis="right_target"):
    return {"right_hand": {"link_name": ee_link, "pose_source": "right_controller",
                           "control_trigger": "right_grip", "vis_target": vis,
                           "control_mode": "pose"}}


