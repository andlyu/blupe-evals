def get(name):
    spec = ARMS.get(name)
    if spec is None:
        raise SystemExit(f"unknown arm {name!r}; available: {', '.join(sorted(ARMS))}")
    if spec.status != "ready":
        raise SystemExit(f"arm {name!r} is {spec.status} — see scripts/arms.py notes "
                         f"and docs/refs/{name}/")
    return spec
