"""Generate the bimanual-YAM sim assets (docs/refs/yam-bimanual; arms.py "yam-bimanual").

Two name-prefixed copies of assets/yam/yam.xml (left_/right_, MjSpec attach — MuJoCo can't
<include> one file twice) composed into:
  assets/yam_bimanual/yam_bimanual.xml   robots-only model (+ combined `home` keyframe)
  assets/yam_bimanual/scene.xml          wrapper: floor, lights, offwidth, left/right mocap targets
  assets/yam_bimanual/yam_bimanual.urdf  matching placo IK model (same MJCF-walk recipe as
                                         gen_yam_urdf.py — link/joint names match the MJCF)

Run in the venv from the repo root:  .venv/bin/python scripts/gen_bimanual_scene.py
"""

import os

import mujoco
import numpy as np
from meshcat import transformations as tf

SRC = "assets/yam/yam.xml"
OUT_DIR = "assets/yam_bimanual"
SEP_Y = 0.35                      # each base this far from the centerline (70 cm apart)

os.makedirs(OUT_DIR, exist_ok=True)

# ---- compose two prefixed YAMs into one robots-only model ---------------------------- #
single = mujoco.MjModel.from_xml_path(SRC)
home6 = single.key("home").qpos[:6].copy()

parent = mujoco.MjSpec()
parent.modelname = "yam_bimanual"
parent.compiler.degree = False
parent.meshdir = os.path.abspath("assets/yam/assets")   # reuse the single-arm meshes

for side, y in (("left", +SEP_Y), ("right", -SEP_Y)):
    child = mujoco.MjSpec.from_file(SRC)
    for k in list(child.keys):                # drop per-arm keyframes; we add a combined one
        child.delete(k)
    frame = parent.worldbody.add_frame(pos=[0, y, 0])
    parent.attach(child, prefix=f"{side}_", frame=frame)

parent.add_key(name="home", qpos=np.concatenate([home6, home6]).tolist(),
               ctrl=np.concatenate([home6, home6]).tolist())
model = parent.compile()                      # validates before we write anything
xml = parent.to_xml().replace(parent.meshdir, "../yam/assets/")   # portable, not abs
with open(f"{OUT_DIR}/yam_bimanual.xml", "w") as f:
    f.write(xml)
print(f"wrote {OUT_DIR}/yam_bimanual.xml  nq={model.nq} nu={model.nu} "
      f"bodies={model.nbody - 1}")

# ---- scene wrapper (mirrors assets/yam/scene.xml conventions) ------------------------ #
TARGET = """    <body name="{n}_target" pos="0.3 {y} 0.4" quat="1 0 0 0" mocap="true">
      <geom name="{n}t_x" type="cylinder" size="0.004 0.04" pos="0.04 0 0" zaxis="1 0 0" rgba="1 0 0 1" contype="0" conaffinity="0" group="1"/>
      <geom name="{n}t_y" type="cylinder" size="0.004 0.04" pos="0 0.04 0" zaxis="0 1 0" rgba="0 1 0 1" contype="0" conaffinity="0" group="1"/>
      <geom name="{n}t_z" type="cylinder" size="0.004 0.04" pos="0 0 0.04" zaxis="0 0 1" rgba="0 0 1 1" contype="0" conaffinity="0" group="1"/>
      <site type="sphere" size="0.008" rgba="0 0 1 1" group="1"/>
    </body>"""

scene = f"""<mujoco model="yam_bimanual_scene">
  <!-- GENERATED by scripts/gen_bimanual_scene.py — edit that, not this. -->
  <include file="yam_bimanual.xml"/>

  <statistic center="0.3 0 0.3" extent="1.4"/>

  <visual>
    <headlight diffuse="0.6 0.6 0.6" ambient="0.3 0.3 0.3" specular="0 0 0"/>
    <global azimuth="120" elevation="-20" offwidth="1920" offheight="720"/>
  </visual>

  <asset>
    <texture type="skybox" builtin="flat" rgb1="0 0 0" rgb2="0 0 0" width="512" height="512"/>
    <texture type="2d" name="groundplane" builtin="checker" mark="edge"
             rgb1="0.2 0.3 0.4" rgb2="0.1 0.2 0.3" markrgb="0.8 0.8 0.8"
             width="300" height="300"/>
    <material name="groundplane" texture="groundplane" texuniform="true" texrepeat="5 5" reflectance="0.2"/>
  </asset>

  <worldbody>
    <light pos="0 0 1.5" dir="0 0 -1" directional="true"/>
    <geom name="floor" size="0 0 0.05" type="plane" material="groundplane"/>
{TARGET.format(n="left", y=+SEP_Y)}
{TARGET.format(n="right", y=-SEP_Y)}
  </worldbody>
</mujoco>
"""
with open(f"{OUT_DIR}/scene.xml", "w") as f:
    f.write(scene)
ms = mujoco.MjModel.from_xml_path(f"{OUT_DIR}/scene.xml")
print(f"wrote {OUT_DIR}/scene.xml  keyframes={[ms.key(i).name for i in range(ms.nkey)]}")

# ---- URDF + FK verification (shared MJCF-walk recipe) -------------------------------- #
import mjcf2urdf  # noqa: E402

m = mujoco.MjModel.from_xml_path(f"{OUT_DIR}/yam_bimanual.xml")
mjcf2urdf.write_urdf(m, f"{OUT_DIR}/yam_bimanual.urdf", "yam_bimanual")
ok = mjcf2urdf.fk_check(m, f"{OUT_DIR}/yam_bimanual.urdf", ["left_grasp", "right_grasp"],
                        q=np.concatenate([home6, home6]) + 0.1)
print("PASS" if ok else "FAIL")
