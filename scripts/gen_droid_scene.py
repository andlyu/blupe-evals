"""Generate DROID sim assets (docs/refs/droid; arms.py "droid").

DROID = Franka Panda (7-DOF) + Robotiq 2F-85. Composed from the vendored MuJoCo Menagerie
models (assets/droid/): the Robotiq attaches at the panda's attachment_site WITH its full
mechanism intact (8 coupled finger joints + tendon + 1 `fingers_actuator`) so the trigger
can actually open/close it in sim. The arm has 7 actuators (indices 0-6) and the gripper's
single actuator lands at index 7 — the eval's d.ctrl[:7] arm slicing stays clean.

The IK URDF is ARM-ONLY (generated straight from panda_nohand.xml): the Robotiq's closed
kinematic loop has joints off their body origins (can't be a URDF tree) and the fingers are
downstream of the EE frame, so placo never needs them. Outputs droid.xml + scene.xml +
droid.urdf.

Run in the venv from the repo root:  .venv/bin/python scripts/gen_droid_scene.py
"""

import os

import mujoco
import numpy as np

import mjcf2urdf

OUT_DIR = "assets/droid"

panda_home = list(mujoco.MjModel.from_xml_path(f"{OUT_DIR}/panda_nohand.xml").key("home").qpos[:7])

parent = mujoco.MjSpec()
parent.modelname = "droid"
parent.compiler.degree = False
parent.meshdir = os.path.abspath(f"{OUT_DIR}/assets")   # panda + robotiq meshes merged here

panda = mujoco.MjSpec.from_file(f"{OUT_DIR}/panda_nohand.xml")
for k in list(panda.keys):
    panda.delete(k)
parent.attach(panda, prefix="", frame=parent.worldbody.add_frame())

grip = mujoco.MjSpec.from_file(f"{OUT_DIR}/robotiq/2f85.xml")
for k in list(grip.keys):                               # keep joints/tendon/equality/actuator
    grip.delete(k)
n_grip_j = len(grip.joints)                             # 8 coupled finger joints
site = next(s for s in parent.sites if s.name == "attachment_site")
parent.attach(grip, prefix="grip_", site=site)

parent.add_key(name="home", qpos=panda_home + [0.0] * n_grip_j, ctrl=panda_home + [0.0])
model = parent.compile()
assert model.nu == 8, f"expected 8 actuators (7 arm + grip), got nu={model.nu}"
assert mujoco.mj_name2id(model, mujoco.mjtObj.mjOBJ_ACTUATOR, "grip_fingers_actuator") == 7, \
    "gripper actuator must be index 7 (after the 7 arm actuators)"
xml = parent.to_xml().replace(parent.meshdir, "assets/")
# MjSpec attach leaves the panda's anonymous root <default> unnamed: re-parses standalone
# but NOT via <include> ("empty class name"). Name it.
xml = xml.replace("<default>\n\n    <default>\n",
                  '<default>\n\n    <default class="panda-root">\n', 1)
with open(f"{OUT_DIR}/droid.xml", "w") as f:
    f.write(xml)
print(f"wrote {OUT_DIR}/droid.xml  nq={model.nq} nu={model.nu}")

scene = """<mujoco model="droid_scene">
  <!-- GENERATED by scripts/gen_droid_scene.py — edit that, not this. -->
  <include file="droid.xml"/>

  <!-- menagerie panda_nohand.xml + 2f85.xml set these; MjSpec attach does NOT propagate
       child <option>s, and the panda's damping needs the implicit integrator -->
  <option integrator="implicitfast" impratio="10" cone="elliptic"/>

  <statistic center="0.3 0 0.4" extent="1.2"/>

  <visual>
    <headlight diffuse="0.6 0.6 0.6" ambient="0.3 0.3 0.3" specular="0 0 0"/>
    <global azimuth="120" elevation="-20" offwidth="1920" offheight="720"/>
  </visual>

  <asset>
    <texture type="skybox" builtin="flat" rgb1="0 0 0" rgb2="0 0 0" width="512" height="512"/>
    <texture type="2d" name="groundplane" builtin="checker" mark="edge"
             rgb1="0.2 0.3 0.4" rgb2="0.1 0.2 0.3" markrgb="0.8 0.8 0.8"
             width="300" height="300"/>
    <material name="groundplane" texture="groundplane" texuniform="true" texrepeat="5 5" reflectance="0.2"/>
  </asset>

  <worldbody>
    <light pos="0 0 2.0" dir="0 0 -1" directional="true"/>
    <geom name="floor" size="0 0 0.05" type="plane" material="groundplane"/>
    <body name="right_target" pos="0.4 0.0 0.5" quat="1 0 0 0" mocap="true">
      <geom name="rt_x" type="cylinder" size="0.004 0.04" pos="0.04 0 0" zaxis="1 0 0" rgba="1 0 0 1" contype="0" conaffinity="0" group="1"/>
      <geom name="rt_y" type="cylinder" size="0.004 0.04" pos="0 0.04 0" zaxis="0 1 0" rgba="0 1 0 1" contype="0" conaffinity="0" group="1"/>
      <geom name="rt_z" type="cylinder" size="0.004 0.04" pos="0 0 0.04" zaxis="0 0 1" rgba="0 0 1 1" contype="0" conaffinity="0" group="1"/>
      <site type="sphere" size="0.008" rgba="0 0 1 1" group="1"/>
    </body>
  </worldbody>
</mujoco>
"""
with open(f"{OUT_DIR}/scene.xml", "w") as f:
    f.write(scene)
ms = mujoco.MjModel.from_xml_path(f"{OUT_DIR}/scene.xml")
print(f"wrote {OUT_DIR}/scene.xml  keyframes={[ms.key(i).name for i in range(ms.nkey)]}")

# IK URDF = ARM ONLY, straight from panda_nohand (the gripper is downstream of the EE and
# its loop-closed joints can't be a URDF tree). panda mounts at the scene's world origin,
# so arm FK matches the scene.
pm = mujoco.MjModel.from_xml_path(f"{OUT_DIR}/panda_nohand.xml")
mjcf2urdf.write_urdf(pm, f"{OUT_DIR}/droid.urdf", "droid")
ok = mjcf2urdf.fk_check(pm, f"{OUT_DIR}/droid.urdf", ["attachment"])

# plant stability at home (the OpenArm lesson: verify rest, don't assume)
d = mujoco.MjData(ms)
mujoco.mj_resetDataKeyframe(ms, d, ms.key("home").id)
mx = 0.0
for i in range(2000):
    mujoco.mj_step(ms, d)
    if i > 400:
        mx = max(mx, float(np.abs(d.qvel).max()))
print(f"steady-state max |qvel|: {mx:.4f} rad/s -> {'OK' if mx < 0.1 else 'UNSTABLE'}")
print("PASS" if ok and mx < 0.1 else "FAIL")
