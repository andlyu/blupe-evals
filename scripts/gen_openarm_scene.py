"""Generate OpenArm v2 bimanual sim assets (docs/refs/openarm; arms.py "openarm").

From the vendored upstream model (assets/openarm/openarm_bimanual_upstream.xml, from
enactic/openarm_mujoco v2) produce a variant our eval can drive:
  - fingers WELDED (finger joints/actuators/mimic constraints removed): upstream has
    nq=18/nu=16 with finger ctrls interleaved per side, which breaks the eval's contiguous
    d.ctrl[:N_ARM] slicing; fingerless gives nq=nu=14 aligned. Same convention as our YAM
    sim (gripper not modeled in sim; the REAL gripper is the serve's job).
  - a `home` keyframe (slight elbow bend) and a scene wrapper with mocap targets.
  - a matching kinematics URDF (mjcf2urdf — names identical to the MJCF).

Run in the venv from the repo root:  .venv/bin/python scripts/gen_openarm_scene.py
"""

import mujoco
import numpy as np

import mjcf2urdf

SRC = "assets/openarm/openarm_bimanual_upstream.xml"
OUT_DIR = "assets/openarm"

spec = mujoco.MjSpec.from_file(SRC)
for j in [j for j in spec.joints if "finger" in j.name]:
    spec.delete(j)
for a in [a for a in spec.actuators if "finger" in a.name]:
    spec.delete(a)
for e in list(spec.equalities):                  # all equalities are finger mimics
    spec.delete(e)

HOME7 = [0.0, 0.0, 0.0, 1.570796, 0.0, 0.0, 0.0]   # elbow 90 deg — upstream pedestal home
spec.add_key(name="home", qpos=HOME7 * 2, ctrl=HOME7 * 2)

model = spec.compile()
assert model.nq == model.nu == 14, f"expected 14/14, got nq={model.nq} nu={model.nu}"
xml = spec.to_xml()
with open(f"{OUT_DIR}/openarm_bimanual.xml", "w") as f:
    f.write(xml)
print(f"wrote {OUT_DIR}/openarm_bimanual.xml  nq={model.nq} nu={model.nu}")

TARGET = """    <body name="{n}_target" pos="0.35 {y} 0.5" quat="1 0 0 0" mocap="true">
      <geom name="{n}t_x" type="cylinder" size="0.004 0.04" pos="0.04 0 0" zaxis="1 0 0" rgba="1 0 0 1" contype="0" conaffinity="0" group="1"/>
      <geom name="{n}t_y" type="cylinder" size="0.004 0.04" pos="0 0.04 0" zaxis="0 1 0" rgba="0 1 0 1" contype="0" conaffinity="0" group="1"/>
      <geom name="{n}t_z" type="cylinder" size="0.004 0.04" pos="0 0 0.04" zaxis="0 0 1" rgba="0 0 1 1" contype="0" conaffinity="0" group="1"/>
      <site type="sphere" size="0.008" rgba="0 0 1 1" group="1"/>
    </body>"""

scene = f"""<mujoco model="openarm_scene">
  <!-- GENERATED by scripts/gen_openarm_scene.py — edit that, not this.
       Mount mirrors upstream pedestal.xml: torso mesh + arms hanging at z=0.698. -->
  <compiler meshdir="assets" angle="radian"/>

  <!-- upstream sets this too: kp=230 position servos RING at the default
       0.002 timestep (measured 460 rad/s oscillation at rest) -->
  <option timestep="0.001"/>

  <statistic center="0.3 0 0.8" extent="1.8"/>

  <visual>
    <headlight diffuse="0.6 0.6 0.6" ambient="0.3 0.3 0.3" specular="0 0 0"/>
    <global azimuth="120" elevation="-20" offwidth="1920" offheight="720"/>
  </visual>

  <asset>
    <texture type="skybox" builtin="flat" rgb1="0 0 0" rgb2="0 0 0" width="512" height="512"/>
    <texture type="2d" name="groundplane" builtin="checker" mark="edge"
             rgb1="0.2 0.3 0.4" rgb2="0.1 0.2 0.3" markrgb="0.8 0.8 0.8"
             width="300" height="300"/>
    <material name="groundplane" texture="groundplane" texuniform="true" texrepeat="5 5" reflectance="0.2"/>
    <material name="openarm_body_link0_material" rgba="0.247 0.247 0.247 1.0" reflectance="0.05" shininess="0.5"/>
    <mesh name="body_link0" content_type="model/stl" file="visual/body/body_link0.stl" scale="0.001 0.001 0.001"/>
    <model name="bimanual" file="openarm_bimanual.xml"/>
  </asset>

  <worldbody>
    <light pos="0 0 2.2" dir="0 0 -1" directional="true"/>
    <geom name="floor" size="0 0 0.05" type="plane" material="groundplane"/>
    <geom name="pedestal" type="mesh" mesh="body_link0" contype="0" conaffinity="0"
          material="openarm_body_link0_material"/>
    <frame pos="0 0 0.698">
      <attach model="bimanual" prefix=""/>
    </frame>
{TARGET.format(n="left", y=+0.25)}
{TARGET.format(n="right", y=-0.25)}
  </worldbody>
</mujoco>
"""
with open(f"{OUT_DIR}/scene.xml", "w") as f:
    f.write(scene)
ms = mujoco.MjModel.from_xml_path(f"{OUT_DIR}/scene.xml")
print(f"wrote {OUT_DIR}/scene.xml  keyframes={[ms.key(i).name for i in range(ms.nkey)]}")

# URDF from the SCENE model (arms mounted at z=0.698): placo's world must match the sim's
# world or clutch engage jumps by the mount offset. Mocap targets become inert fixed links.
mjcf2urdf.write_urdf(ms, f"{OUT_DIR}/openarm_bimanual.urdf", "openarm_bimanual")
ok = mjcf2urdf.fk_check(ms, f"{OUT_DIR}/openarm_bimanual.urdf",
                        ["openarm_left_ee_base_link", "openarm_right_ee_base_link"])
print("PASS" if ok else "FAIL")
